// for loops
console.log('for loops');
for (let i = 0; i < 10; i++) {
  console.log(i + 1);
}

// counting down
console.log('for loops - counting down');
for (let i = 25; i >= 0; i -= 5) {
  console.log(i);
}

// while loop
console.log('while loops');
let count = 0;
while (count < 10) {
  count++;
  console.log(count);
}
